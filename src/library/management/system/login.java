    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        // TODO add your handling code here:
        PreparedStatement pst;
        ResultSet rs;
        Connection c = Connect.COnnection();
        try {
            pst=c.prepareStatement("select * from login where user_id=? and password=?");
            String userid=txtuserid.getText();
            String password = new String(txtpassword.getPassword());
            String hashedPassword = Utils.hashPassword(password);  // hash the entered password
            pst.setString(1,userid );
            pst.setString(2, hashedPassword);
            rs=pst.executeQuery();
            if(rs.next()){
                Home home = new Home(this);
                home.setVisible(true);
                this.dispose();
            }
            else
                JOptionPane.showMessageDialog(this, "Please enter valid id password");
        } catch (SQLException ex) {
            System.getLogger(login.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }    
    }//GEN-LAST:event_loginbtnActionPerformed
