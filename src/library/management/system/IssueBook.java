    private void btnissuebookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnissuebookActionPerformed
        // TODO add your handling code here:
        String bid = issuebid.getText().trim();
        String sid= issuesid.getText().trim();
        String bname = issuebname.getText().trim();
        String idate = issuedate.getText().trim();
        String ddate = duedate.getText().trim();
        
        if(bid.isEmpty()||sid.isEmpty()||bname.isEmpty()||idate.isEmpty()||ddate.isEmpty()){
            JOptionPane.showMessageDialog(this, "All fields are required.");
            return;
        }
        try {
            //check if student exist
            PreparedStatement stcheck = con.prepareStatement("SELECT * FROM STUDENT WHERE id =?");
            stcheck.setString(1, sid);
            ResultSet rs1 = stcheck.executeQuery();
            if(!rs1.next()){
                JOptionPane.showMessageDialog(this, "No student available with ID: " +sid);
                return;
            }
            //check if book is alredy issued
            PreparedStatement bookcheck = con.prepareStatement("SELECT STATUS FROM books WHERE bid=?");
            bookcheck.setString(1, bid);
            ResultSet rs2 = bookcheck.executeQuery();
            if(rs2.next() && "ISSUED".equalsIgnoreCase(rs2.getString("STATUS"))){
                JOptionPane.showMessageDialog(this, "Book is alredy issued.");
                return;
            }
            //Insert into issue table
            PreparedStatement pst = con.prepareStatement("UPDATE books set status='ISSUED',issue_date=?,due_date=?,student_id=? WHERE bid=? ");
            pst.setString(1, idate);
            pst.setString(2, ddate);
            pst.setString(3, sid);
            pst.setString(4, bid);
            int rownAffected=pst.executeUpdate();
            if(rownAffected >0){
                JOptionPane.showMessageDialog(this, "Book issued Successfully");
                issuebid.setText("");
                issuesid.setText("");
                issuebname.setText("");
                issuedate.setText("");
                duedate.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this, "Failed to issue book. Check Book ID.");
            }
        } catch (SQLException ex) {
            System.getLogger(IssueBook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        
        
    }//GEN-LAST:event_btnissuebookActionPerformed

