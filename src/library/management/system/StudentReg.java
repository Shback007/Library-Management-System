    private void StudentbtnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StudentbtnSubmitActionPerformed
        // TODO add your handling code here:
        String idStr = Sid.getText();
        String name = Sname.getText();
        String course = Scourse.getText();
        String branch = Sbranch.getText();
        String sem = Ssem.getText();
        
        if(idStr.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Sid.requestFocus();
        }
        else if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Sname.requestFocus();
        }
        else if(course.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Scourse.requestFocus();
        }
        else if(branch.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Sbranch.requestFocus();
        }
        else if(sem.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Ssem.requestFocus();
        }
        else{
            Connection con =Connect.COnnection();
            int id = Integer.parseInt(idStr);
            try {
                PreparedStatement check = con.prepareStatement("SELECT * FROM STUDENT WHERE id =?");
                check.setInt(1, id);
                ResultSet rs=check.executeQuery();
                
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Student ID alredy exists.");
                    return;
                }
                PreparedStatement pst = con.prepareStatement("INSERT INTO STUDENT(id,name,course,branch,semester) VALUES(?,?,?,?,?)");
                pst.setInt(1, id);
                pst.setString(2, name);
                pst.setString(3, course);
                pst.setString(4, branch);
                pst.setString(5, sem);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Saved");
                 clearFormFields();
            } catch (SQLException ex ) {
                System.getLogger(StudentReg.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    }//GEN-LAST:event_StudentbtnSubmitActionPerformed

