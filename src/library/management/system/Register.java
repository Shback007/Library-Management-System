    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String username = reguser.getText();
        String password = new String(regpass.getPassword());
            
        if(username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "All fields are required");
            return;
        }
        Connection c = Connect.COnnection();
        try {
            // TODO add your handling code here:
            PreparedStatement checkStmt = c.prepareStatement("SELECT * FROM login WHERE user_id = ?");
            checkStmt.setString(1, username);
            ResultSet rs = checkStmt.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this,  "Username already exists. Please choose another one.");
                return;
            }
            PreparedStatement pst = c.prepareStatement("INSERT INTO LOGIN(user_id,password) VALUES (?, ?)");
            pst.setString(1, username);
            String hashedpassword= Utils.hashPassword(password);
            pst.setString(2, hashedpassword);
            int result = pst.executeUpdate();
            
            if(result >0){
                JOptionPane.showMessageDialog(this, "Registration sucessful. Please Login");
                loginForm.setEnabled(true);  // Re-enable login form
                loginForm.toFront();
                this.dispose();
            }else{
                JOptionPane.showMessageDialog((this), "Registration failed");
            
            }
        } catch (SQLException ex) {
            System.getLogger(Register.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

