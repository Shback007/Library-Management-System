    private void btnAddbookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddbookActionPerformed
        // TODO add your handling code here:
        String id=Bid.getText();
        String name=Bname.getText();
        String publisher=Bpub.getText();
        String price = Bprice.getText();
        String year = Byear.getText();
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Bid.requestFocus();
        }
        else if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Bname.requestFocus();
        }
        else if(publisher.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Bpub.requestFocus();
        }
        else if(price.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Bprice.requestFocus();
        }
        else if(year.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields are required");
            Byear.requestFocus();
        }else{
            Connection con = Connect.COnnection();
            try { 
                PreparedStatement check = con.prepareStatement("SELECT * FROM BOOKS WHERE bid=?");
                check.setString(1, id);
                ResultSet rs=check.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Book ID alredy exists.");
                    return;
                }
                PreparedStatement pst = con.prepareStatement("INSERT INTO BOOKS (bid,name,publisher,price,year,status) VALUES(?,?,?,?,?,?)");
                pst.setString(1, id);
                pst.setString(2, name);
                pst.setString(3, publisher);
                pst.setString(4, price);
                pst.setString(5, year);
                pst.setString(6, "NOT-ISSUED");
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Saved");
                clearFormFields();
            } catch (SQLException ex) {
                System.getLogger(Addbook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
            
        }
    }//GEN-LAST:event_btnAddbookActionPerformed

